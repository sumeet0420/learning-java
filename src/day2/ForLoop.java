package day2;

public class ForLoop {

    public static void main(String[] args) {
        //For Loop; initialization; condition; increment/decrement
        //i++ => i=i+1
        //             2<5 => true
        //i=3
        for ( int i=2; i<5; i++) {
            System.out.println("Hello at i = "  + i);
        }

        System.out.println("****************");
        for ( int i=5; i>=5; i--){
            System.out.println("Hello at i = "  + i);
        }

    }
}
